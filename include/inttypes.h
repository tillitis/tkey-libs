#define PRIu8  "u"
#define PRIu16 "u"
#define PRIu32 "u"

/* Lowercase hexadecimal notation */
#define PRIx8  "x"
#define PRIx16 "x"
#define PRIx32 "x"

/* Uppercase hexadecimal notation */
#define PRIX8  "X"
#define PRIX16 "X"
#define PRIX32 "X"

#endif /* INTTYPES_H */
